numero = prompt ("Digite um número");
// Vamos calcular o número digitado em busca do resto da divisão
// Se o resto for igual a 0(zero), então o número digitado é Par
// Caso contrário, será ímpar 
if (numero % 2 == 0){
    alert("O número digitado é Par")
}
else{
    alert("O número digitado é Ímpar")
}